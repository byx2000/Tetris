void CMainFrame::OnClose()
{
	CTetrisDoc* pDoc = (CTetrisDoc*)GetActiveDocument();
	CTetrisView* pView = (CTetrisView*)GetActiveView();
	bool flag = false;
	if (pDoc->getCurrentState() == GameController::RUNNING)
	{
		pView->pauseGame();
		flag = true;
	}

	int ret = MessageBox(TEXT("您确定要退出吗？"), TEXT("提示"), MB_OKCANCEL | MB_ICONINFORMATION);
	if (ret == IDCANCEL)
	{
		if (flag)
		{
			pView->startGame();
		}
		return;
	}

	pView->updateRecord();

	//保存音乐标记
	std::ofstream fout("tmp\\musicFlag");
	fout << pView->getMusicFlag();

	CFrameWnd::OnClose();
}


int CMainFrame::OnCreate(LPCREATESTRUCT lpCreateStruct)
{
	if (CFrameWnd::OnCreate(lpCreateStruct) == -1)
		return -1;

	//弹出首次运行对话框
	if (!PathFileExists(TEXT("tmp\\firstFlag")))
	{
		CFirstDlg dlg;
		dlg.DoModal();
		std::ofstream fout("tmp\\firstFlag");
		fout << 1;
	}

	return 0;
}



void CMainFrame::OnActivate(UINT nState, CWnd* pWndOther, BOOL bMinimized)
{
	CFrameWnd::OnActivate(nState, pWndOther, bMinimized);

	// 窗口失去焦点后暂停游戏
	if (nState == WA_INACTIVE)
	{
		CTetrisView* pView = (CTetrisView*)GetActiveView();
		pView->pauseGame();
	}
	
}
